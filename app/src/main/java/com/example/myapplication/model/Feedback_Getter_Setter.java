package com.example.myapplication.model;

public class Feedback_Getter_Setter {
    String id,name,email,mobile,writefeedback;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getWritefeedback() {
        return writefeedback;
    }

    public void setWritefeedback(String writefeedback) {
        this.writefeedback = writefeedback;
    }
}
